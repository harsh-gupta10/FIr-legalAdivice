export const i18n = new I18n(translations)
i18n.enableFallback = true
i18n.defaultLocale = 'en'
i18n.locale = 'en'

const supported = new Set(LANGUAGE_OPTIONS.map((item) => item.code))

export const getDeviceLanguage = () => {
  const locales = getLocales()
  const primary = locales && locales.length > 0 ? locales[0] : null
  const languageCode = primary?.languageCode || 'en'
  return supported.has(languageCode) ? languageCode : 'en'
}

export const setI18nLanguage = (lang) => {
  const normalized = supported.has(lang) ? lang : 'en'
  i18n.locale = normalized
  return normalized
}

export const getCurrentLanguage = () => {
  return supported.has(i18n.locale) ? i18n.locale : 'en'
}

export const t = (key, options = {}) => {
  return i18n.t(key, options)
}

export const tForLocale = (locale, key, options = {}) => {
  return i18n.t(key, { locale, ...options })
}

export const getLocaleTag = (language) => {
  if (language === 'hi') return 'hi-IN'
  if (language === 'te') return 'te-IN'
  return 'en-IN'
}

export const getOffenceLabel = (value) => {
  const map = {
    Theft: 'offence.theft',
    Assault: 'offence.assault',
    Fraud: 'offence.fraud',
    Harassment: 'offence.harassment',
    Other: 'offence.other',
  }

  return t(map[value] || 'offence.other')
}
